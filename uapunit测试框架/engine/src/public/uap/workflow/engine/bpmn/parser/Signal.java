package uap.workflow.engine.bpmn.parser;
public class Signal {
	protected String id;
	  protected String errorCode;
	  
	  public String getId() {
	    return id;
	  }
	  
	  public void setId(String id) {
	    this.id = id;
	  }
	  
	  public String getErrorCode() {
	    return errorCode;
	  }
	  
	  public void setErrorCode(String errorCode) {
	    this.errorCode = errorCode;
	  }

}
